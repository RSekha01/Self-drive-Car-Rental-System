    public void autoID() {
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost/rental", "root", "");
        Statement s = con.createStatement();
        ResultSet rs = s.executeQuery("SELECT MAX(car_no) FROM users");

        if (rs.next()) {
            String maxCarNo = rs.getString(1); // Get the first column result (MAX(car_no))
            if (maxCarNo == null || maxCarNo.isEmpty()) {
                txtreg_no.setText("C001");
            } else {
                long id = Long.parseLong(maxCarNo.substring(1)); // Assume car_no format is "Cxxx"
                id++;
                txtreg_no.setText("C" + String.format("%03d", id)); // Format with leading zeros
            }
        }
    } catch (ClassNotFoundException ex) {
        Logger.getLogger(carreg.class.getName()).log(Level.SEVERE, null, ex);
    } catch (SQLException ex) {
        Logger.getLogger(carreg.class.getName()).log(Level.SEVERE, null, ex);
    }
}

