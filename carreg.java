    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String rgno=txtreg_no.getText();
        String make=txtbrand.getText();
        String mod=txtmodel.getText();
       
        String aval=txtav.getSelectedItem().toString();
         
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/rental","root","");
            pst = con.prepareStatement("insert into users ( car_no, Brand, Model,Status) Values (?, ?, ?,?)");
            pst.setString(1,rgno);
            pst.setString(2,make);
            pst.setString(3,mod);
            
            pst.setString(4,aval);
           
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"car added.....");
            txtbrand.setText("");
            txtmodel.setText("");
            txtav.setSelectedIndex(-1);
            txtbrand.requestFocus();
            autoID();
            table_update();
        }catch(ClassNotFoundException ex){
            Logger.getLogger(carreg.class.getName()).log(Level.SEVERE,null, ex);
            
        } catch (SQLException ex) {
            Logger.getLogger(carreg.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

