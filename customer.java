    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
                  
    String custID = txtcustid.getText();
    String custname = txtcustname.getText();
    String address = txtaddress.getText();
    String mobile = txtmobile.getText();

    // Validate the length of the mobile number
    if (mobile.length() > 15) {
        JOptionPane.showMessageDialog(this, "Mobile number is too long. Please enter a valid mobile number.");
        return;
    }

    Connection con = null;
    PreparedStatement pst = null;
    
    try {
        // Load MySQL JDBC driver
        Class.forName("com.mysql.cj.jdbc.Driver");

        // Establish connection
        con = DriverManager.getConnection("jdbc:mysql://localhost/rental", "root", "");

        // Prepare SQL query
        String sql = "INSERT INTO customer (cust_id, name, address, mobile) VALUES (?, ?, ?, ?)";
        pst = con.prepareStatement(sql);

        // Set parameters
        pst.setString(1, custID);
        pst.setString(2, custname);
        pst.setString(3, address);
        pst.setString(4, mobile);

        // Execute update
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Customer added successfully.");

        // Clear input fields and reset focus
        txtcustname.setText("");
        txtaddress.setText("");
        txtmobile.setText("");
        txtcustname.requestFocus();

        // Call additional methods
        autoID();
        table_update();
    } catch (ClassNotFoundException ex) {
        Logger.getLogger(customer.class.getName()).log(Level.SEVERE, "JDBC Driver not found", ex);
    } catch (SQLException ex) {
        Logger.getLogger(customer.class.getName()).log(Level.SEVERE, "Database error", ex);
    } finally {
        // Close resources
        try {
            if (pst != null) pst.close();
            if (con != null) con.close();
        } catch (SQLException ex) {
            Logger.getLogger(customer.class.getName()).log(Level.SEVERE, "Error closing resources", ex);
        }
    }

    }//GEN-LAST:event_jButton4ActionPerformed

