    public void autoID() {
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost/rental", "root", "");
        Statement s = con.createStatement();
        ResultSet rs = s.executeQuery("SELECT MAX(cust_id) FROM customer");

        if (rs.next()) {
            String maxcustid = rs.getString(1); // Get the first column result (MAX(car_no))
            if (maxcustid == null || maxcustid.isEmpty()) {
                txtcustid.setText("A001");
            } else {
                long id = Long.parseLong(maxcustid.substring(1)); // Assume car_no format is "Cxxx"
                id++;
                txtcustid.setText("A0" + String.format("%03d", id)); // Format with leading zeros
            }
        }
    } catch (ClassNotFoundException ex) {
        Logger.getLogger(customer.class.getName()).log(Level.SEVERE, null, ex);
    } catch (SQLException ex) {
        Logger.getLogger(customer.class.getName()).log(Level.SEVERE, null, ex);
    }
}

