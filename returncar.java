    private void txtcaridKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcaridKeyPressed

        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/rental", "root", "");
                String car_id = txtcarid.getText();
                pst = con.prepareStatement("SELECT car_id, cust_id, due, DATEDIFF(CURDATE(), due) AS elap FROM car_rental WHERE car_id = ?");
                pst.setString(1, car_id);
                rs = pst.executeQuery();

                if(rs.next() == false) {
                    JOptionPane.showMessageDialog(this, "Car ID not found");
                } else {
                    String custid = rs.getString("cust_id");
                    txtcustid.setText(custid.trim());

                    String date = rs.getString("due");
                    txtdate.setText(date.trim());

                    String elp = rs.getString("elap");
                    System.out.println("Elapsed days: " + elp); // Debug output
                    int elaped = Integer.parseInt(elp);

                    if(elaped > 0) {
                        txtelp.setText(elp);
                        int fine = elaped * 100;
                        System.out.println("Fine: " + fine); // Debug output
                        txtFine.setText(String.valueOf(fine));
                    } else {
                        txtelp.setText("0");
                        txtFine.setText("0");
                    }
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(returncar.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(returncar.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NumberFormatException ex) {
                System.err.println("Error parsing the elapsed days: " + ex.getMessage()); // Debug output
            }
        }

    }//GEN-LAST:event_txtcaridKeyPressed

