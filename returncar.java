    private void txtokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtokActionPerformed

        String car_id = txtcarid.getText();
        String cust_id = txtcustid.getText();
        String date = txtdate.getText();
        String elp = txtelp.getText();
        String fine = txtFine.getText();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/rental", "root", "");

            // Insert the return car details into the returncar table
            pst = con.prepareStatement("INSERT INTO returncar(carid, custid, return_date, elap, fine) VALUES (?, ?, ?, ?, ?)");
            pst.setString(1, car_id);       // Use actual column name for car ID
            pst.setString(2, cust_id);      // Use actual column name for customer ID
            pst.setString(3, date);         // Use actual column name for return date
            pst.setString(4, elp);          // Use actual column name for elapsed time
            pst.setString(5, fine);         // Use actual column name for fine amount
            pst.executeUpdate();

            // Delete the rental record from the car_rental table
            pst1 = con.prepareStatement("DELETE FROM car_rental WHERE car_id = ?");
            pst1.setString(1, car_id);       // Use actual column name for car ID in car_rental table
            pst1.executeUpdate();

            // Update the status of the car in the users table to 'Available'
            pst2 = con.prepareStatement("UPDATE users SET Status = 'Available' WHERE car_no = ?");
            pst2.setString(1, car_id);       // Use actual column name for car ID in users table
            pst2.executeUpdate();

            JOptionPane.showMessageDialog(this, "Car returned successfully");

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(returncar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(returncar.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (pst != null) pst.close();
                if (pst1 != null) pst1.close();
                if (pst2 != null) pst2.close();
                if (con != null) con.close();
            } catch (SQLException ex) {
                Logger.getLogger(returncar.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_txtokActionPerformed

